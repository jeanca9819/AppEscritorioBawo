    public void columnas(){
        ArrayList<Object> nombreColumna = new ArrayList<Object>();
        nombreColumna.add("Código");
        nombreColumna.add("Nombre");
        nombreColumna.add("Link");
        
        for (Object columna : nombreColumna) {
            modelo.addColumn(columna);
        }
        
        this.jtImagenes.setModel(modelo);
    }
   
    public void cargarDatos() {
        
        ArrayList<domain.Imagen> imagenes = this.imagenBusiness.getImagenes();
        
        for (domain.Imagen imagen : imagenes) {
            Object[] imagen2 = new Object[]{
                imagen.getCodigoImagen(),
                imagen.getNombre(),
                imagen.getUrlImagen()
            };
            modelo.addRow(imagen2);
        }
        
        this.jtImagenes.setModel(modelo);
        
    }
    
    public void limpiarModelo() {
        
        if (modelo.getRowCount() > 0) {
            for (int i = modelo.getRowCount() - 1; i > -1; i--) {
                modelo.removeRow(i);
            }
        }
    }
    
    
