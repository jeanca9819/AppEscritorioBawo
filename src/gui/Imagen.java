    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String nombre = jtfName.getText();
        String link = jtfLink.getText();
        
        if (nombre.equals("") || link.equals("")) {
            jlMessageError.setText("Llene el campo solicitado por favor.");
            jlMessageSuccess.setText(""); 
        }else{
            
            domain.Imagen imagen = new domain.Imagen();
            imagen.setNombre(nombre);
            imagen.setUrlImagen(link);
            
            if (this.imagenBusiness.addImagen(imagen) == 1) {
                jlMessageSuccess.setText("Se ingresó correctamente la imagen.");  
                jlMessageError.setText("");
                this.limpiarModelo();
                this.cargarDatos();
            }else{
                jlMessageError.setText("Hubo un problema al ingresar la imagen, intenta de nuevo.");
                jlMessageSuccess.setText(""); 
            }
        }
        jtfName.setText("");
        jtfLink.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

